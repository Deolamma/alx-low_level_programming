#include "main.h"
#include <string.h>
/**
 * _strcat - concatenates two strings
 *
 * @dest: The string from source is appended to dest
 * @src: string from source is added to the string in dest
 *
 * Return: returns a concatenated string of src to dest
 */
char *_strcat(char *dest, char *src)
{
	return (strcat(dest, src));
}

